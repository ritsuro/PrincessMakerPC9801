	INC	DI
	INC	DI
	LODSW			;
	AND	WORD PTR ES:[DI],AX
	INC	DI
	INC	DI
	ADD	DI,ADDWIDTH	;
	LOOP	VRAM_PUT2

	MOV	SI,OFFSET CUSDAT0 ; カーソル
	MOV	DI,BX		; 先頭オフセット
	MOV	CX,CUSLINE	; カーソルライン数
VRAM_PUT22:
