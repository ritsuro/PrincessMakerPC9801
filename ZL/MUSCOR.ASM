	ADD	DI,ADDWIDTHCRT	; カーソル用計算4
	ADD	DI,ADDWIDTHCRT	; カーソル用計算5
	ADD	DI,ADDWIDTHCRT	; カーソル用計算6
	ADD	DI,ADDWIDTHCRT	; カーソル用計算7
	ADD	DI,1		;
	MOV	AX,ARAVRM[6]	; vram area.
	MOV	ES,AX		;
	MOV	CX,CUSLINE	; カーソルライン数
VRAM_PUT2_C:
