VRAM_LOAD5:
	MOVSW			;
	MOVSW			;
	MOVSB			;
	ADD	DI,ADDWIDTH_SV	; エリア退避用計算

	LOOP	VRAM_LOAD5

	RET
VRAM_LOAD	ENDP


;	ＶＲＡＭカーソルＰＵＴ

VRAM_PUT	PROC	NEAR
	MOV	BX,CURY		; カーソルＹ
	SHL	BX,1		;
