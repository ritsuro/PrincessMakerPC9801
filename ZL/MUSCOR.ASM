	LOOP	VRAM_PUT33


	MOV	SI,OFFSET CUSMSK; カーソルマスク
	MOV	DI,BX		; 先頭オフセット
	MOV	AX,ARAVRM[4]	; vram area.
	MOV	ES,AX		;
	MOV	CX,CUSLINE	; カーソルライン数
VRAM_PUT4:
