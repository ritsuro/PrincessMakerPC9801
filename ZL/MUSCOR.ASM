CODE	SEGMENT	PUBLIC	'CODE'
	ASSUME	CS:CODE,DS:DATA

	PUBLIC	MSCSRT		; カーソルスタート
	PUBLIC	MSCMOV		; カーソル移動
	PUBLIC	MSCWAT		; カーソルボタンＯＦＦ待ち
	PUBLIC	MSCSTP		; カーソルストップ

	EXTRN	MUSINI:NEAR	; マウスの初期化
	EXTRN	MUSRED:NEAR	; マウス読み込み
	EXTRN	MUSSTP:NEAR	; マウスの終了


ADDWIDTHCRT	EQU	80	; カーソル用計算
ADDWIDTH	EQU	80-4	; カーソル用計算
ADDWIDTH_SV	EQU	80-5	; エリア退避用計算
CUSLINE		EQU	32	; カーソルライン数
CUSLINE_SV	EQU	40	; エリア退避ライン数
MUSMAX_X	EQU	80-5	; マウス座標最大Ｘ
MUSMAX_Y	EQU	50-5	; マウス座標最大Ｙ
OFST_X		EQU	2	; マウス座標オフセットＸ
OFST_Y		EQU	2	; マウス座標オフセットＹ

;	カーソルスタート

MSCSRT	PROC	NEAR
	CALL	MUSRED		; マウス読み込み
	CALL	ADJMOV		; 移動量調整

	MOV	CURX,AX
	MOV	CURY,BX

	CALL	VRAM_SAVE	; ＶＲＡＭ退避

	CALL	VRAM_PUT	; ＶＲＡＭカーソルＰＵＴ

	RET
MSCSRT	ENDP


;	カーソルストップ

MSCSTP	PROC	NEAR
	CALL	VRAM_LOAD	; ＶＲＡＭ戻す
	RET
MSCSTP	ENDP


;	カーソル移動
;	-O- AX : Ｘ座標
;	    BX : Ｙ座標
;	    DH : 左ボタン（1：ＯＦＦ／0：ＯＮ）
;	    DL : 右ボタン（1：ＯＦＦ／0：ＯＮ）

MSCMOV	PROC	NEAR
	CALL	MUSRED		; マウス読み込み
	CALL	ADJMOV		; 移動量調整

	CMP	AX,CURX		; カーソルＸ
	JNE	MSCMOV5

	CMP	BX,CURY		; カーソルＹ
	JNE	MSCMOV5

	JMP	MSCMOV9
MSCMOV5:
	PUSH	AX
	PUSH	BX
	PUSH	DX

	CALL	VRAM_LOAD	; ＶＲＡＭ戻す

	MOV	AX,CURNEWX	; カーソル新しいＸ
	MOV	CURX,AX
	MOV	AX,CURNEWY	; カーソル新しいＹ
	MOV	CURY,AX

	CALL	VRAM_SAVE	; ＶＲＡＭ退避
	CALL	VRAM_PUT	; ＶＲＡＭカーソルＰＵＴ

	POP	DX
	POP	BX
	POP	AX
MSCMOV9:
	ADD	AX,OFST_X
	ADD	BX,OFST_Y
	RET
MSCMOV	ENDP


;	カーソルボタンＯＦＦ待ち

MSCWAT	PROC	NEAR
MSCWAT1:
	CALL	MUSRED	; マウス読み込み
	CMP	DH,0	; 左ボタン（1：ＯＦＦ／0：ＯＮ）
	JZ	MSCWAT1	
	CMP	DL,0	; 右ボタン（1：ＯＦＦ／0：ＯＮ）
	JZ	MSCWAT1	

	RET
MSCWAT	ENDP



;	移動量調整
;	-I/O- AX : Ｘ座標
;	      BX : Ｙ座標
;	-O-   CURNEWX : カーソル新しいＸ
;	      CURNEWY : カーソル新しいＹ

ADJMOV	PROC	NEAR
	SAR	AX,1	; AX = AX / 8
	SAR	AX,1	;
	SAR	AX,1	;
	SAR	BX,1	; BX = BX / 8
	SAR	BX,1	;
	SAR	BX,1	;

	TEST	AX,8000H
	JZ	ADJMOV20
	MOV	AX,0
	JMP	ADJMOV2
ADJMOV20:
	CMP	AX,MUSMAX_X	; マウス座標最大Ｘ
	JLE	ADJMOV2
	MOV	AX,MUSMAX_X	; マウス座標最大Ｘ
ADJMOV2:
	MOV	CURNEWX,AX	; カーソル新しいＸ

	TEST	BX,8000H
	JZ	ADJMOV30
	MOV	BX,0
	JMP	ADJMOV3
ADJMOV30:
	CMP	BX,MUSMAX_Y	; マウス座標最大Ｙ
	JLE	ADJMOV3		;
	MOV	BX,MUSMAX_Y	; マウス座標最大Ｙ
ADJMOV3:
	MOV	CURNEWY,BX	; カーソル新しいＹ
	RET
ADJMOV	ENDP



;	ＶＲＡＭ退避

VRAM_SAVE	PROC	NEAR
	PUSH	DS

	MOV	BX,CURY		; カーソルＹ
	SHL	BX,1		;
	SHL	BX,1		;
	SHL	BX,1		; bx = vy1 * 8

	SHL	BX,1		;
	SHL	BX,1		;  source point.
	SHL	BX,1		;
	SHL	BX,1		;  bx = vy1 * 80 + vx1
	MOV	AX,BX		;
	SHL	BX,1		;
	SHL	BX,1		;
	ADD	BX,AX		;
	ADD	BX,CURX		; カーソルＸ

	MOV	SI,BX		; 先頭オフセット

	MOV	DI,OFFSET VRMSAV0	; ＶＲＡＭ退避エリア

	MOV	AX,DS		; データセグメント
	MOV	ES,AX		;

	MOV	DX,DS

	MOV	AX,ARAVRM[0]	; vram area.
	MOV	DS,AX		;
	MOV	CX,CUSLINE_SV	; エリア退避ライン数
VRAM_SAVE2:
	MOVSW			;
	MOVSW			;
	MOVSB			;
	ADD	SI,ADDWIDTH_SV	; エリア退避用計算

	LOOP	VRAM_SAVE2

	MOV	SI,BX		; 先頭オフセット

	MOV	DS,DX		;

	MOV	AX,ARAVRM[2]	; vram area.
	MOV	DS,AX		;
	MOV	CX,CUSLINE_SV	; エリア退避ライン数
VRAM_SAVE3:
	MOVSW			;
	MOVSW			;
	MOVSB			;
	ADD	SI,ADDWIDTH_SV	; エリア退避用計算

	LOOP	VRAM_SAVE3

	MOV	SI,BX		; 先頭オフセット

	MOV	DS,DX		;

	MOV	AX,ARAVRM[4]	; vram area.
	MOV	DS,AX		;
	MOV	CX,CUSLINE_SV	; エリア退避ライン数
VRAM_SAVE4:
	MOVSW			;
	MOVSW			;
	MOVSB			;
	ADD	SI,ADDWIDTH_SV	; エリア退避用計算

	LOOP	VRAM_SAVE4

	MOV	SI,BX		; 先頭オフセット

	MOV	DS,DX		;

	MOV	AX,ARAVRM[6]	; vram area.
	MOV	DS,AX		;
	MOV	CX,CUSLINE_SV	; エリア退避ライン数
VRAM_SAVE5:
	MOVSW			;
	MOVSW			;
	MOVSB			;
	ADD	SI,ADDWIDTH_SV	; エリア退避用計算

	LOOP	VRAM_SAVE5

	POP	DS
	RET
VRAM_SAVE	ENDP



;	ＶＲＡＭ戻す

